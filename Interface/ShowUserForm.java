    public void recibeDatos(int id, String nombre, String apellido, String tipodocumento, String documento, String correo){
         txtId.setText(String.valueOf(id));
         txtNombre.setText(nombre);
         txtApellidos.setText(apellido);
         txtTipoDocumento.setText(tipodocumento);
         txtDocumento.setText(documento);
         txtCorreo.setText(correo);
    }
    
    public void actualizarEmpleado(){
        //Se consulta el valor que tien el textfield
        int id= Integer.parseInt(txtId.getText());
        String nombre= txtNombre.getText();
        String apellido= txtApellidos.getText();
        String correo= txtCorreo.getText();
        
        if(nombre.isEmpty() || apellido.isEmpty() || correo.isEmpty()){
        JOptionPane.showMessageDialog(null, "Hay campos sin diligenciar", "Informaci√≥n", JOptionPane.WARNING_MESSAGE);
        }else{
            String query= "UPDATE `empleaados` SET `nombre`='"+nombre+"',`apelldio`='"+apellido+"',`correo`='"+correo+"' WHERE idempleado="+id+"";
            System.out.println(query);
            try{
                connection= conexion.getConnection();
                st= connection.createStatement();
                st.executeUpdate(query);
                JOptionPane.showMessageDialog(this, "Se ha actualizado al Usuario");
            }catch(SQLException e){
                JOptionPane.showMessageDialog(this, "No se puedo actualizar al Usuario");
            }
            this.dispose();
        }
        
        
    }
    
    public void EliminarEmpleado(){
        int id= Integer.parseInt(txtId.getText());
        String query="DELETE FROM `empleaados` WHERE idempleado ='"+id+"';";
         try{
            connection= conexion.getConnection();
            st= connection.createStatement();
            st.executeUpdate(query);
            JOptionPane.showMessageDialog(this, "Se ha eliminado al usuario al Usuario");
         }catch(SQLException e){
            JOptionPane.showMessageDialog(this, "No se pudo eliminar al Usuario","", JOptionPane.ERROR_MESSAGE);
         }
         this.dispose();
    }

